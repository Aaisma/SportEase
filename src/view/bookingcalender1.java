private void loadVenueDetails() {
    try {
        Connection con = MySqlConnection.getConnection();
        String sql = "SELECT location FROM venue WHERE venue_name=?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, selectedVenue);

        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            jLabel8.setText(rs.getString("location")); // LOCATION LABEL
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
