private void loadAvailableTimeSlots() {

    LocalTime openTime = LocalTime.of(10, 0);
    LocalTime closeTime = LocalTime.of(21, 0);

    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("hh:mm a");
    Set<String> booked = new HashSet<>();

    try {
        Connection con = MySqlConnection.getConnection();

        String sql = """
            SELECT booking_time FROM booking
            WHERE venue_name = ? AND booking_date = CURDATE()
        """;

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, selectedVenue);

        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            booked.add(rs.getString("booking_time"));
        }

    } catch (Exception e) {
        e.printStackTrace();
    }

    DefaultListModel<String> model = new DefaultListModel<>();
    LocalTime now = LocalTime.now();
    LocalTime time = openTime;

    while (time.plusHours(1).isBefore(closeTime.plusSeconds(1))) {

        LocalTime next = time.plusHours(1);
        String slot = time.format(formatter) + " - " + next.format(formatter);

        // ðŸ”¥ Only future slots today
        if (time.isAfter(now) && !booked.contains(slot)) {
            model.addElement(slot);
        }

        time = next;
    }

    jComboBox1.removeAllItems();
    for (int i = 0; i < model.size(); i++) {
        jComboBox1.addItem(model.get(i));
    }
}

      
